/* 
    An identifier identifies a variable, a function or property
    An identifier may start with underscore, dollar sign or letters
*/

// Variable name identifier
const variableName = 'Some value'; // variableName is an identifier

// Function name identifier
function functionName() { // functionName is an identifier
    console.log();
}

const obj = {
    propertyName: 'John' // propertyName is an identifier
};
