/* 
    Ternary operator
    - accepts three operands
    - it is if-else conditional expression (if-else condition is a statement)


*/

/*
    Unary operator
    
    !true

    - it accepts only one operand
*/

/*
    Binary operator

    1 == '1'

    - it accepts two operands


*/

/*
    Comparison operators are with higher priority that logical operators
*/

const a = 5;
const b = -3;
console.log(a > 0 && b > 0); // false

//          not false -> true
console.log(!(a > 0 && b > 0)); // true
